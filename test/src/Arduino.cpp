/******************************************************************************
 * INCLUDE
 ******************************************************************************/

#include <Arduino.h>

/******************************************************************************
 * GLOBAL VARIABLES
 ******************************************************************************/

static unsigned long current_millis = 0;

/******************************************************************************
 * PUBLIC FUNCTIONS
 ******************************************************************************/

void set_millis(unsigned long const millis)
{
  current_millis = millis;
}

unsigned long millis()
{
  return current_millis;
}
